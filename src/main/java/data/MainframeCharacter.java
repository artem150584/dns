package data;

public class MainframeCharacter {
    private String name;
    private String price;
    private String warranty;
    private String operationSystem;
    private String cpuModel;
    private String cpuNumber;
    private String cpuClockSpeed;
    private String videoCardModel;
    private String videoCardMemory;
    private String ramType;
    private String ramSize;
    private String hddSize;
    private String sddSize;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getWarranty() {
        return warranty;
    }

    public void setWarranty(String warranty) {
        this.warranty = warranty;
    }

    public String getOperationSystem() {
        return operationSystem;
    }

    public void setOperationSystem(String operationSystem) {
        this.operationSystem = operationSystem;
    }

    public String getCpuModel() {
        return cpuModel;
    }

    public void setCpuModel(String cpuModel) {
        this.cpuModel = cpuModel;
    }

    public String getCpuNumber() {
        return cpuNumber;
    }

    public void setCpuNumber(String cpuNumber) {
        this.cpuNumber = cpuNumber;
    }

    public String getCpuClockSpeed() {
        return cpuClockSpeed;
    }

    public void setCpuClockSpeed(String cpuClockSpeed) {
        this.cpuClockSpeed = cpuClockSpeed;
    }

    public String getVideoCardModel() {
        return videoCardModel;
    }

    public void setVideoCardModel(String videoCardModel) {
        this.videoCardModel = videoCardModel;
    }

    public String getVideoCardMemory() {
        return videoCardMemory;
    }

    public void setVideoCardMemory(String videoCardMemory) {
        this.videoCardMemory = videoCardMemory;
    }

    public String getRamType() {
        return ramType;
    }

    public void setRamType(String ramType) {
        this.ramType = ramType;
    }

    public String getRamSize() {
        return ramSize;
    }

    public void setRamSize(String ramSize) {
        this.ramSize = ramSize;
    }

    public String getHddSize() {
        return hddSize;
    }

    public void setHddSize(String hddSize) {
        this.hddSize = hddSize;
    }

    public String getSddSize() {
        return sddSize;
    }

    public void setSddSize(String sddSize) {
        this.sddSize = sddSize;
    }
}
